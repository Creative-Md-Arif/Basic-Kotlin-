package Basic

fun main(args: Array<String>) {
//    Number
//    Byte
    val a: Byte = 10
    println(a)
    println(Byte.MAX_VALUE)
    println(Byte.MIN_VALUE)

//    Short
    val b: Short = 20
    println(b)
    println(Short.MAX_VALUE)
    println(Short.MIN_VALUE)

//    Int
    val c: Int = 30
    println(c)
    println(Int.MAX_VALUE)
    println(Int.MIN_VALUE)


//    Long
    val d: Long = 40
    println(d)
    println(Long.MAX_VALUE)
    println(Long.MIN_VALUE)


//    Float
    val e: Float = 20.00f
    println(e)

//    Double
    val f: Double = 20.00
    println(f)
//    Cher

    val g: Char = 'A'
    println(g)

    val h: Char = 65.toChar()
    println(h)
//    Boolean

    val i: Boolean = true
    println(i)


}